    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        Usuari user = da.getUser(txtUsuari.getText());
        if (user != null) {
            char[] passwordToVerify = txtPasswordLogin.getPassword();
            String passwordHashUserDb = user.getPasswordHash();
            var result = BCrypt.verifyer().verify(passwordToVerify, passwordHashUserDb);
            if (result.verified) {
                JOptionPane.showMessageDialog(this, "Login successful. Welcome " + user.getNom());
                this.setVisible(false);
                authenticated = true;
                userConnected = user;
            } else {
                JOptionPane.showMessageDialog(this, "Error: invalid password.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error: user not found.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

